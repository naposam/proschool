
                      <script type="text/javascript">
						    	function letteronly(input){
						    		var regex=/[^-a-z]/gi;
						    		input.value=input.value.replace(regex,"");
						    	}
						   
						   </script>
						  <script>
						
						    	function numberonly(input){
						    		var regex=/[^0-9]/g;
						    		input.value=input.value.replace(regex,"");
						    	}
						
                     </script>